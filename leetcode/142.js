var detectCycle = function(head) {
  let slow = head
  let fast = head
  while (true) {
    if (!fast || !fast.next) return null
    slow = slow.next
    fast = fast.next.next
    if (slow === fast) break
  }
  fast = head
  while (fast != slow) {
    slow = slow.next
    fast = fast.next
  }
  return slow
}
